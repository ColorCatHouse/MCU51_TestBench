#include <STC89.H>

void delay(void)
{
	unsigned int t;
	
	for(t=0;t<1000;t++);
}

void main(void)
{
	unsigned char hex[16]={0x3F, 0x06, 0x5B, 0x4F,
						 0x66, 0x6D, 0x7D, 0x07,
						 0x7F, 0x67, 0x77, 0x7C,
						 0x39, 0x5E, 0x79, 0x71};

	for (;;)
	{
		P0=~hex[0xA];
		P1=0xE;	  
		delay();

		P0=~hex[0xB];
		P1=0xD;
		delay();

		P0=~hex[0xC];
		P1=0xB;
		delay();

		P0=~hex[0xD];
		P1=0x7;
		delay();
	}
}